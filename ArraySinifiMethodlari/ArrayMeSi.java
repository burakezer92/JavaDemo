package ArraySinifiMethodlari;

public class ArrayMeSi {
    void method(int[] a){
        System.out.print("[");
        for (int i=0;i<a.length;i++){
            System.out.print(a[i]+" ");
        }
        System.out.print("]");
    }
    void method(double[] a){
        System.out.print("[");
        for (int i=0;i<a.length;i++){
            System.out.print(a[i]+" ");
        }
        System.out.print("]");
    }
}
